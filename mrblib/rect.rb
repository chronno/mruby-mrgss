#============================================================================
module MRGSS
    #------------------------------------------------------------------------
    # * Rect
    #------------------------------------------------------------------------
    # This Class represents a Rectangle
    #------------------------------------------------------------------------
    class Rect
        #--------------------------------------------------------------------
        # Acessors
        #--------------------------------------------------------------------
        attr_accessor :x
        attr_accessor :y
        attr_accessor :width
        attr_accessor :height
        #--------------------------------------------------------------------
        # Equals
        #--------------------------------------------------------------------
        def ==(other) 
            return false unless other.is_a? MRGSS::Rect
            return (@x == other.x and @y == other.y and @width = other.width and @height = other.height)
        end
    end
end